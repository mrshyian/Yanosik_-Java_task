package com.javatask.model;

public class VehicleModel {
    private String carBrand;
    private String carModel;
    private String carBoughtDate;
    private String insurerName;
    private String insurancePrice;
    private String insuranceDate;

    public VehicleModel() {
    }

    public String getCarBrand() {
        return carBrand;
    }

    public void setCarBrand(String carBrand) {
        this.carBrand = carBrand;
    }

    public String getCarModel() {
        return carModel;
    }

    public void setCarModel(String carModel) {
        this.carModel = carModel;
    }

    public String getCarBoughtDate() {
        return carBoughtDate;
    }

    public void setCarBoughtDate(String carBoughtDate) {
        this.carBoughtDate = carBoughtDate;
    }

    public String getInsurerName() {
        return insurerName;
    }

    public void setInsurerName(String insurerName) {
        this.insurerName = insurerName;
    }

    public String getInsurancePrice() {
        return insurancePrice;
    }

    public void setInsurancePrice(String insurancePrice) {
        this.insurancePrice = insurancePrice;
    }

    public String getInsuranceDate() {
        return insuranceDate;
    }

    public void setInsuranceDate(String insuranceDate) {
        this.insuranceDate = insuranceDate;
    }
}
